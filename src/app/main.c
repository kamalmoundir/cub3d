static t_config	*get_config(void);

int	main(int argc, char **argv)
{
	t_config	*conf;
	int			x;

	(void)argc;
	(void)argv;
	conf = get_config();
	x = 0;
	while(!conf->is_done)
	{
		x = 0;
		while (x < screenWidth)
		{
			conf->cameraX = 2 * x / (double)screenWidth;
			conf->rayDirX = conf->dirX + conf->planeX * conf->cameraX;
			conf->rayDirY = conf->dirY + conf->planeY * conf->cameraX;
		}
	}
	return (0);
}

static t_config	*get_config(void)
{
	t_config	*conf;

	conf = malloc(sizeof(t_config));
	if (!conf)
		return (NULL);
	conf->is_done = 0;
	conf->posX = 22;
	conf->posY = 12;
	conf->dirX = -1;
	conf->dirY = 0;
	conf->planeX = 0;
	conf->planeY = 0.66;
	conf->time = 0;
	conf->oldTime = 0;
	conf->cameraX = 0; // change later
	conf->rayDirY = 0; // change later
	conf->rayDirX = 0; // change later
	return (conf);
}